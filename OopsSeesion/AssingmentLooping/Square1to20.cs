using System;
using System.Collections.Generic;
using System.Text;

namespace OopsSeesion.AssingmentLooping
{
    class Square1to20
    {
        static void Main(string[] args)
        {
            for(int i=1;i<=20;i++)
            {
                int square = i * i;
                Console.WriteLine(square);
            }
        }
    }
}
